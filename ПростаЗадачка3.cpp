#include <iostream>
using namespace std;

int main() {
    int a, b;

    cout << "Введіть число a:";
    cin >> a;
    cout << "Введіть число b:";
    cin >> b;

    cout << "Сума:" << (a + b) << endl;
    cout << "Різниця:" << (a - b) << endl;
    cout << "Добуток:" << (a * b) << endl;
    return 0;
}
